// ─── Renderer ─────────────────────────────────────────────────────────────────

export type PixelIconName = keyof typeof ICONS;

interface PixelIconProps {
  name: PixelIconName;
  size?: number;        // rendered px size
  color?: string;       // fill color for pixels
  className?: string;
}

export default function PixelIcon({
  name,
  size = 20,
  color = "#0a0a1a",
  className = "",
}: PixelIconProps) {
  const grid = ICONS[name];
  if (!grid) return null;

  const rows = grid.length;
  const cols = grid[0].length;
  const cellSize = 10; // each pixel = 10 SVG units
  const viewW = cols * cellSize;
  const viewH = rows * cellSize;

  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${viewW} ${viewH}`}
      fill="none"
      className={className}
      aria-hidden="true"
    >
      {grid.map((row, r) =>
        row.map((cell, c) =>
          cell === 1 ? (
            <rect
              key={`${r}-${c}`}
              x={c * cellSize}
              y={r * cellSize}
              width={cellSize}
              height={cellSize}
              fill={color}
            />
          ) : null
        )
      )}
    </svg>
  );
}
